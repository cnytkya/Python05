#Kendi modüllerimizi oluşturabileceğimiz gibi, py'ın zengin standart kütüphanesinde yer alan yüzlerce modülü de kullanabiliriz.
import math  # Bu modül,matematiksel fonksiyonlar içerir(sinüs,kosinüs,karekökt vs.)

