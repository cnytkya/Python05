# int veri tipi: int veri tipi tam sayıları tutar. int ifadesi matematiksel işlemler için kullanılır.

a = 15
b = 20

print(a + b)  # Toplama işlemi
print(a - b)  # Çıkarma işlemi
print(a * b)  # Çarpma işlemi
print(b / a)  # Bölme işlemi
print(b // a) # Tam sayı bölme işlemi
print(b % a)  # Modülüs(mod alma) işlemi